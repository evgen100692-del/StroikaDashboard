async function run() {
  const SQL = await initSqlJs();
  if (!fs.existsSync(DB_PATH)) {
    console.error('БД не найдена:', DB_PATH);
    process.exit(1);
  }

  const fileBuffer = fs.readFileSync(DB_PATH);
  const db = new SQL.Database(fileBuffer);

  const res = db.exec('SELECT id, objectName, completed FROM contracts');
  if (!res.length) { console.log('Контрактов в БД нет'); return; }
  const contracts = res[0].values.map(([id, objectName, completed]) => ({ id, objectName, completed }));
  console.log(`Контрактов в БД: ${contracts.length}`);

  let updated = 0;
  const notFound = [];

  for (const [name, value] of Object.entries(completedData)) {
    // 1. Точное совпадение
    let match = contracts.find(c => c.objectName === name);

    // 2. Совпадение без суффиксов ФБ / КИК / ИБК
    if (!match) {
      const clean = name.replace(/\s*(ФБ|КИК|ИБК)\s*$/, '').trim();
      match = contracts.find(c => c.objectName && c.objectName.replace(/\s*(ФБ|КИК|ИБК)\s*$/, '').trim() === clean);
    }

    // 3. Совпадение по первым 60 символам (fuzzy)
    if (!match) {
      const fragment = name.replace(/\s*(ФБ|КИК|ИБК)\s*$/, '').trim().slice(0, 60);
      match = contracts.find(c => c.objectName && c.objectName.includes(fragment));
    }

    if (match) {
      db.run('UPDATE contracts SET completed = ? WHERE id = ?', [value, match.id]);
      console.log(`✅ [id=${match.id}] ${match.objectName.slice(0, 70)} => ${value.toLocaleString('ru-RU')} руб.`);
      updated++;
    } else {
      notFound.push(name.slice(0, 90));
    }
  }

  const data = db.export();
  fs.writeFileSync(DB_PATH, Buffer.from(data));

  console.log(`\n✅ Обновлено: ${updated} контрактов`);
  if (notFound.length) {
    console.log(`\n⚠️  Не найдено совпадений в БД (${notFound.length}):`);
    notFound.forEach(n => console.log('   -', n));
    console.log('   (Проверьте наименования объектов в БД)');
  }
}

run().catch(err => {
  console.error('Ошибка:', err);
  process.exit(1);
});
